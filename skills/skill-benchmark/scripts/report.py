def load_template(template_path: Path) -> str:
    """Load the HTML template file."""
    if not template_path.is_file():
        print(f"Error: template not found at '{template_path}'", file=sys.stderr)
        sys.exit(1)
    return template_path.read_text(encoding="utf-8")


def merge_scores(score_files: list[Path]) -> dict:
    """Load and optionally merge multiple score files.

    If multiple files are provided, averages across runs for variance analysis.
    """
    all_scores = []
    for sf in score_files:
        if not sf.is_file():
            print(f"Warning: score file not found at '{sf}', skipping", file=sys.stderr)
            continue
        all_scores.append(json.loads(sf.read_text(encoding="utf-8")))

    if not all_scores:
        print("Error: no valid score files found", file=sys.stderr)
        sys.exit(1)

    if len(all_scores) == 1:
        return all_scores[0]

    # Merge multiple runs: average the aggregates
    base = all_scores[0]
    num_runs = len(all_scores)

    # Average summary
    for key in ("win_rate", "overall_delta"):
        base["summary"][key] = round(
            sum(s["summary"].get(key, 0) for s in all_scores) / num_runs, 4
        )
    base["summary"]["wins"] = round(
        sum(s["summary"].get("wins", 0) for s in all_scores) / num_runs, 1
    )
    base["summary"]["num_runs"] = num_runs

    # Average per-dimension
    for dim in base.get("dimensions", {}):
        for metric in ("avg_with", "avg_without", "avg_delta"):
            vals = [s["dimensions"].get(dim, {}).get(metric, 0) for s in all_scores]
            base["dimensions"][dim][metric] = round(sum(vals) / len(vals), 4)

    return base


def build_radar_data(scores: dict) -> str:
    """Build JSON data for the radar chart (per-dimension comparison)."""
    dimensions = scores.get("dimensions", {})
    labels = list(dimensions.keys())
    with_values = [dimensions[d].get("avg_with", 0) for d in labels]
    without_values = [dimensions[d].get("avg_without", 0) for d in labels]
    return json.dumps(
        {
            "labels": labels,
            "with_values": with_values,
            "without_values": without_values,
        }
    )


def build_tasks_data(scores: dict) -> str:
    """Build JSON data for the bar chart (per-task delta)."""
    tasks = scores.get("tasks", [])
    labels = [t.get("task_id", f"task-{i}") for i, t in enumerate(tasks)]
    deltas = [t.get("delta", 0) for t in tasks]
    with_avgs = [t.get("average_with", 0) for t in tasks]
    without_avgs = [t.get("average_without", 0) for t in tasks]
    prompts = [t.get("prompt", "") for t in tasks]
    return json.dumps(
        {
            "labels": labels,
            "deltas": deltas,
            "with_avgs": with_avgs,
            "without_avgs": without_avgs,
            "prompts": prompts,
        }
    )


def build_aggregate_data(scores: dict) -> str:
    """Build JSON for summary statistics."""
    summary = scores.get("summary", {})
    dimensions = scores.get("dimensions", {})
    total_tasks = summary.get("total_tasks", 0)
    runs_per_task = summary.get("runs_per_task", 1)
    total_calls = summary.get("total_calls", total_tasks * 2)
    return json.dumps(
        {
            "total_tasks": total_tasks,
            "wins": summary.get("wins", 0),
            "losses": summary.get("losses", 0),
            "ties": summary.get("ties", 0),
            "win_rate": summary.get("win_rate", 0),
            "overall_delta": summary.get("overall_delta", 0),
            "num_runs": summary.get("num_runs", 1),
            "runs_per_task": runs_per_task,
            "total_calls": total_calls,
            "dimensions": {
                dim: {
                    "avg_with": v.get("avg_with", 0),
                    "avg_without": v.get("avg_without", 0),
                    "avg_delta": v.get("avg_delta", 0),
                }
                for dim, v in dimensions.items()
            },
        }
    )


def render(template: str, scores: dict, lang: str = "en") -> str:
    """Replace template placeholders with computed data and translations."""
    translations = TRANSLATIONS.get(lang, TRANSLATIONS["en"])
    skill_name = scores.get("skill", "Unknown Skill")
    replacements = {
        "{{SKILL_NAME}}": skill_name,
        "{{RADAR_DATA}}": build_radar_data(scores),
        "{{TASKS_DATA}}": build_tasks_data(scores),
        "{{AGGREGATE_DATA}}": build_aggregate_data(scores),
        "{{TRANSLATIONS_JSON}}": json.dumps(translations, ensure_ascii=False),
    }
    result = template
    for placeholder, value in replacements.items():
        result = result.replace(placeholder, value)
    # Apply translation placeholders
    for key, value in translations.items():
        result = result.replace("{{" + key + "}}", value)
    return result


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(
        description="Generate an HTML benchmark report from scores.json.",
        epilog=(
            "Example:\n"
            "  python report.py --scores scores.json --template assets/report-template.html --output report.html\n\n"
            "Multiple runs:\n"
            "  python report.py --scores run1.json run2.json --template assets/report-template.html --output report.html"
        ),
        formatter_class=argparse.RawDescriptionHelpFormatter,
    )
    parser.add_argument(
        "--scores",
        type=Path,
        nargs="+",
        required=True,
        help="Path(s) to scores.json file(s). Multiple files are averaged.",
    )
    parser.add_argument(
        "--template",
        type=Path,
        required=True,
        help="Path to the HTML template file (assets/report-template.html)",
    )
    parser.add_argument(
        "--output",
        type=Path,
        default=Path("report.html"),
        help="Output path for the HTML report (default: report.html)",
    )
    parser.add_argument(
        "--lang",
        type=str,
        default="en",
        choices=sorted(TRANSLATIONS.keys()),
        help="Report language (default: en). Supported: " + ", ".join(sorted(TRANSLATIONS.keys())),
    )
    args = parser.parse_args(argv)

    template = load_template(args.template)
    scores = merge_scores(args.scores)
    html = render(template, scores, lang=args.lang)

    args.output.parent.mkdir(parents=True, exist_ok=True)
    args.output.write_text(html, encoding="utf-8")

    summary = scores.get("summary", {})
    print(f"Report generated: {args.output}")
    print(f"  Skill: {scores.get('skill', 'unknown')}")
    print(f"  Overall delta: {summary.get('overall_delta', 0):+.4f}")
    print(f"  Win rate: {summary.get('win_rate', 0):.0%}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
